import re
class Solution:
    def mostCommonWord(self, paragraph: str, banned: List[str]) -> str:
        paragraph = paragraph.lower()
        symbols = {'!','?',',',';','.'}
        #\ |\!|\?|\,|\;|\.
        words = filter(None, map(lambda word: word[:-1] if len(word) > 0 and word[-1] in symbols else word, re.split('\W+', paragraph)))
        bannedWords = set(banned)
        wordCt = sorted(collections.Counter(words).items(), key=lambda pair: pair[1])[::-1]
        for word in wordCt:
            if word and word[0] not in bannedWords:
                return word[0]

